    private void jMenu1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu1MouseClicked
        CtrRecibo r = new CtrRecibo();
        int linhaSel; //variavel que recebera a linha(estudante) da tabela selecionada
        int id; //variavel que recebera o id do estudante selecionado

        linhaSel = jTable1.getSelectedRow();  //variavel 'linhaSel' recebera a linha da tabela selecionada 

        if(linhaSel == -1){
            JOptionPane.showMessageDialog(null,"Nenhum recibo selecionado!");
        } else {
            id = (int) jTable1.getValueAt(linhaSel,0); //variavel 'id' recebera o valor das coordenadas selecionada(linhaSel e coluna zero para receber o id do estudante) 
        
            try{ 
                if (JOptionPane.showConfirmDialog(null,"Deseja realmente alterar o status desse recibo?")==JOptionPane.OK_OPTION){ //notificação que pergunta se o usuario realmente quer excluir o estudante selecionado
                    try{
                        if(r.editaStatus(id)==true){ //envia a variavel 'id' para o metodo deletaEstudante na classe Controle)
                            JOptionPane.showMessageDialog(null,"Recibo pago!"); //abrirá uma janela informando que o estudate foi deletado com sucesso
                        }
                    } catch (Exception e){
                        JOptionPane.showMessageDialog(null, "Erro ao alterar o status!\nERRO:"+e.getMessage()); //caso ocorra algum erro, abrirá uma janela informando que aconteceu algo de errado e o erro que ocorreu  
                    }    
                }
            
            } catch (Exception e){
                JOptionPane.showMessageDialog(null,"ERRO:"+e); //caso nao seja selecionada nenhuma linha da tabela, abrira uma tela de notificação infromando que o usuário não selecionou nenhum estudante (linha)
            }
        }
    }//GEN-LAST:event_jMenu1MouseClicked

    private void jMenu2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu2MouseClicked
        int linhaSel; //variavel que recebera a linha(estudante) da tabela selecionada
        int id; //variavel que recebera o id do estudante selecionado

        linhaSel = jTable1.getSelectedRow();  //variavel 'linhaSel' recebera a linha da tabela selecionada

        if(linhaSel == -1){
            JOptionPane.showMessageDialog(null,"Nenhum recibo selecionado!");
        } else {
            Recibo recibo = new Recibo();
            
            id = (int) jTable1.getModel().getValueAt(jTable1.getSelectedRow() ,0);
            String cliente = (String) jTable1.getModel().getValueAt(jTable1.getSelectedRow() ,1);
            String contato = (String) jTable1.getModel().getValueAt(jTable1.getSelectedRow() ,2);
            String telefone = (String) jTable1.getModel().getValueAt(jTable1.getSelectedRow() ,3);
            String email = (String) jTable1.getModel().getValueAt(jTable1.getSelectedRow() ,4);
            
            recibo.setIdrecibo(id);
            recibo.setCliente(cliente);
            recibo.setContato(contato);
            recibo.setTelefone(telefone);
            recibo.setEmail(email);
                
            try {
                Model.DAO bd = Model.DAO.getInstancia();
                Connection con;
                con = bd.getConexao();
                InputStream fonte = gerarReciboo.class.getResourceAsStream("/Relatórios/report2.jrxml");
                JasperReport report = JasperCompileManager.compileReport(fonte);
                Map parametros = new HashMap();
                parametros.put("id", id);
                parametros.put("cliente", cliente);
                parametros.put("contato", contato);
                parametros.put("telefone", telefone);
                parametros.put("email", email);
                JasperPrint print = JasperFillManager.fillReport(report, parametros, con);
                JasperViewer.viewReport(print, false);
            } catch (JRException ex) {
                Logger.getLogger(gerarReciboo.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro ao gerar o recibo!");
            }
                
        }
    }//GEN-LAST:event_jMenu2MouseClicked

