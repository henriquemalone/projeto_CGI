    public void preencherTabela(){ //metodo que preenche a tabela
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        DefaultTableModel tabela; //cria o objeto 'tabela' da coleção DefaultTableModel
        tabela = (DefaultTableModel) jTable1.getModel(); //cria o objeto 'tabela'
        for(Recibo r: CtrRecibo.exibeRecibos()){ 
            tabela.addRow(new Object[]{ //adiciona linha na tabela
                r.getIdrecibo(), //preenche coluna ID
                r.getCliente(), //preenche coluna titulo
                r.getContato(), //preenche coluna id do exemplar
                r.getTelefone(), //preenhce coluna nome do estudante
                r.getEmail(),
                formato.format(r.getData()),
                r.getStatus(),
                r.getPrevisao(),
            });
       }
    }
    
    public void resultadoBusca(String id, String cliente){ //metodo que preenche a tabela
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        CtrRecibo ctrrecibo = new CtrRecibo();

        if(ctrrecibo.procuraRecibo(id, cliente).isEmpty()){
            limparTabela();
            JOptionPane.showMessageDialog(null, "Nenhum recibo encontrado!");
            preencherTabela();
        } else {
            DefaultTableModel tabela; //cria o objeto 'tabela' da coleção DefaultTableModel
            tabela = (DefaultTableModel) jTable1.getModel(); //cria o objeto 'tabela'
            for(Recibo r: ctrrecibo.procuraRecibo(id, cliente)){ //laço que preenche a tabela com cada elemento da lista vinda do médoto exibeEmprestimo da classe Controle
                tabela.addRow(new Object[]{ //adiciona linha na tabela
                    r.getIdrecibo(), //preenche coluna ID
                    r.getCliente(), //preenche coluna titulo
                    r.getContato(), //preenche coluna id do exemplar
                    r.getTelefone(), //preenhce coluna nome do estudante
                    r.getEmail(),
                    formato.format(r.getData()),
                    r.getStatus(),
                    r.getPrevisao(),
                });
            }
        }
    }
    
    public void limparTabela(){
        DefaultTableModel tabela = (DefaultTableModel)jTable1.getModel();
        tabela.setNumRows(0);
    }
    
