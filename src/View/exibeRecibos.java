    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = edtNome.getText();
        String cliente = edtCliente.getText();
        
        if(id.equals("") && cliente.equals("")){
            JOptionPane.showMessageDialog(null,"Nenhum dado digitado!");
            limparTabela();
            preencherTabela();
        } else {
            if (id.equals("")){
                id = "0";
            }
            if(cliente.equals("")){
                cliente = "0";
            }
                 
            //JOptionPane.showMessageDialog(null,id+cliente);
        
            limparTabela();
            resultadoBusca(id,cliente);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

