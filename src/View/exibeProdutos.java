    private void jMenu1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu1MouseClicked
        int linhaSel; //variavel que recebera a linha(estudante) da tabela selecionada
        int id; //variavel que recebera o id do estudante selecionado

        linhaSel = jTable1.getSelectedRow();  //variavel 'linhaSel' recebera a linha da tabela selecionada 

        if(linhaSel == -1){
            JOptionPane.showMessageDialog(null,"Nenhum produto selecionado!");
        } else {
            id = (int) jTable1.getValueAt(linhaSel,0);
            cadProduto cadproduto =  new cadProduto();
            cadproduto.preencherFormulario(id);
            cadproduto.alteraFormulario("    ALTERAR PRODUTO");
            cadproduto.setVisible(true);
        }
    }//GEN-LAST:event_jMenu1MouseClicked

    private void jMenu2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu2MouseClicked
        Produto p = new Produto();
        int linhaSel; //variavel que recebera a linha(estudante) da tabela selecionada
        int id; //variavel que recebera o id do estudante selecionado

        linhaSel = jTable1.getSelectedRow();  //variavel 'linhaSel' recebera a linha da tabela selecionada 

        if(linhaSel == -1){
            JOptionPane.showMessageDialog(null,"Nenhum produto selecionado!");
        } else {
            id = (int) jTable1.getValueAt(linhaSel,0); //variavel 'id' recebera o valor das coordenadas selecionada(linhaSel e coluna zero para receber o id do estudante) 
        
            try{ 
                if (JOptionPane.showConfirmDialog(null,"Deseja realmente apagar o registro desse produto?")==JOptionPane.OK_OPTION){ //notificação que pergunta se o usuario realmente quer excluir o estudante selecionado
                    try{
                        if(p.deletaProduto(id)==true){ //envia a variavel 'id' para o metodo deletaEstudante na classe Controle)
                            JOptionPane.showMessageDialog(null,"Produto deletado com sucesso!"); //abrirá uma janela informando que o estudate foi deletado com sucesso
                        }
                    } catch (Exception e){
                        JOptionPane.showMessageDialog(null, "Erro ao deletar o Produto!\nERRO:"+e.getMessage()); //caso ocorra algum erro, abrirá uma janela informando que aconteceu algo de errado e o erro que ocorreu  
                    }    
                }
            
            } catch (Exception e){
                JOptionPane.showMessageDialog(null,"ERRO:"+e); //caso nao seja selecionada nenhuma linha da tabela, abrira uma tela de notificação infromando que o usuário não selecionou nenhum estudante (linha)
            }
        }
    }//GEN-LAST:event_jMenu2MouseClicked

