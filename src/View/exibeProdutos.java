    public void preencherTabela(){ //metodo que preenche a tabela
        DefaultTableModel tabela; //cria o objeto 'tabela' da coleção DefaultTableModel
        tabela = (DefaultTableModel) jTable1.getModel(); //cria o objeto 'tabela'
        for(Produto p: CtrProduto.exibeProdutos()){ //laço que preenche a tabela com cada elemento da lista vinda do médoto exibeEmprestimo da classe Controle
            tabela.addRow(new Object[]{ //adiciona linha na tabela
                p.getId(), //preenche coluna ID
                p.getNome(), //preenche coluna titulo
                p.getLote(), //preenche coluna id do exemplar
                p.getValor(), //preenhce coluna nome do estudante
                p.getQtd(),
            });
       }
    }
    
    public void limparTabela(){
        DefaultTableModel tabela = (DefaultTableModel)jTable1.getModel();
        tabela.setNumRows(0);
    }
    
    public void resultadoBusca(String nome){ //metodo que preenche a tabela
        CtrProduto ctrproduto = new CtrProduto();

        if(ctrproduto.procuraProduto(0, nome).isEmpty()){
            limparTabela();
            JOptionPane.showMessageDialog(null, "Nenhum produto encontrado!");
            preencherTabela();
        } else {
            DefaultTableModel tabela; //cria o objeto 'tabela' da coleção DefaultTableModel
            tabela = (DefaultTableModel) jTable1.getModel(); //cria o objeto 'tabela'
            for(Produto p: ctrproduto.procuraProduto(0, nome)){ //laço que preenche a tabela com cada elemento da lista vinda do médoto exibeEmprestimo da classe Controle
                tabela.addRow(new Object[]{ //adiciona linha na tabela
                    p.getId(), //preenche coluna ID
                    p.getNome(), //preenche coluna titulo
                    p.getLote(), //preenche coluna id do exemplar
                    p.getValor(), //preenhce coluna nome do estudante
                    p.getQtd(),
                });
            }
        }
    }
    
